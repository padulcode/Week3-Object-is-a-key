function highestScore (students) {
    // Code disini
    const kelas = {}

    // if kosong
    if (students.length == 0) {
      return []
    }

    // menambahkan object dalam object kelas
    for (const i of students) {
      kelas[i.class] = {name : i.name, score : i.score};
    }

    // membandaingkan nilai tertinggi dari tiap class
    for (const key in kelas) {      
      for (const i of students) {
        if(i.class == key && i.score > kelas[key].score){
          kelas[key].score = i.score ;
          kelas[key].name = i.name;
        }
      }      
    }

    return kelas;
  }
  
